package com.learnjava.learnings;

public class LearnBreakContinue {

	public static void main(String[] args) {
		// Learn Break & COntinue

		for (int i = 0; i <= 11; i++) {

			System.out.println("This Value of I: " + i);
			if (i == 4) {
				break;
			}
		}

		for (int j = 0; j < 11; j++) {
			System.out.println("This is correct");
		}
	}

}
