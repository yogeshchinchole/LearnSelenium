package com.learnjava.learnings;

public class LearnBreakSwitch {

	public static void main(String[] args) {
		// Learn switch & Break

		int a = 10;

		switch (a) {

		case 1:
			System.out.println("This is Value of 1 & it is: " + a);
			break;
		case 2:
			System.out.println("This is Value of 2 & it is:" + a);
			break;
		case 3:
			System.out.println("This is Value of 3 & it is:" + a);
			break;
		case 4:
			System.out.println("This is Value of 4 & it is:" + a);
			break;
		case 5:
			System.out.println("This is Value of 5 & it is:" + a);
			break;
		default:
			System.out.println("This other Value");
		}

	}

}
